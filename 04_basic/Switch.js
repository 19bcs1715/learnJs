const month = "january"
switch (month) {
  case "january":
    console.log("january");
    break;
  case "Feb":
    console.log("February");
    break;
  case "March":
    console.log("March");
    break;
  case "April":
    console.log("April");
    break;

  default:
    console.log("No matches");
    break;
}